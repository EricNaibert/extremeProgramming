package extremeProgramming;

public class TiposPrimitivos {
	
	public static void main(String [] args) {
		
		//casting :)
		
		int num1 = 127;
		System.out.println("Int = " + num1);
		
		short num2 = (short)num1;
		System.out.println("Short = " + num2);
		
		byte num3 = (byte)num1;
		System.out.println("Byte = " + num3);
		
	}

}
