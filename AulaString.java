package extremeProgramming;

import java.util.*;

public class AulaString {
	
	public static void main(String[] args) {
		
		Scanner input = new Scanner(System.in);
		String nome;
		
		System.out.println("Digite seu nome: ");
		nome = input.nextLine();
		
		System.out.println(nome);
		
		input.close();
	}

}
